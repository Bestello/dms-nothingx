def calculate_crc16(data: bytes) -> bytes:
    crc = 0xFFFF
    for b in data:
        crc = CRC16_ANSI_TAB[(crc ^ b) & 0xFF] ^ (crc >> 8)
    return bytes([crc & 0xFF, (crc >> 8) & 0xFF])

def build_packet(command_code: int, is_write: bool, payload: List[int], operation_id: int = 1) -> bytes:
    """Construct a full protocol packet.
    
    Args:
        command_code: The hex code for the feature (e.g., 0x18).
        is_write: True for SET/WRITE commands, False for GET/READ commands.
        payload: List of byte integers to send.
        operation_id: Defaults to 1.
        
    Returns:
        The full byte string ready for transmission.
    """
    direction = 0xF0 if is_write else 0xC0
    length = len(payload)
    base_payload = bytes([0x55, 0x60, 0x01, command_code, direction, length, 0x00, operation_id] + payload)
    checksum = calculate_crc16(base_payload)
    return base_payload + checksum

