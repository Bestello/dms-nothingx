def encode_custom_eq_payload(bass: int, mid: int, treble: int) -> List[int]:
    """Encodes custom EQ values into the 60-byte payload structure.
    
    Args:
        bass: Bass level (-6 to +6).
        mid: Mid level (-6 to +6).
        treble: Treble level (-6 to +6).
        
    Returns:
        The full 60-byte payload list.
    """
    def encode_eq_float(value: float, is_total: bool = False) -> List[int]:
        if is_total and value >= 0.0:
            return [0x00, 0x00, 0x00, 0x80]
        
        import struct
        bytes_val = list(struct.pack('<f', value))
        # Handle special negative float encoding logic
        if value != 0.0 and bytes_val[0] == 0x00 and bytes_val[1] == 0x00 and bytes_val[2] == 0x00:
            bytes_val[3] |= 0x80
            
        # The JS code reversed the bytes manually, but struct.pack('<f') gives little-endian
        # JS uses DataView.setFloat32(..., false) which is BIG-endian!
        # Then they return [bytes[3], bytes[2], bytes[1], bytes[0]], which reverses big-endian back to little-endian.
        # So it's effectively just little-endian. We can just use struct.pack('<f') directly.
        return bytes_val

    # Hardcoded base payload template from reference
    payload = [
        0x03, 0x00, 0x00, 0x00, 0x00, 0x01, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x75, 0x44,
        0xc3, 0xf5, 0x28, 0x3f, 0x02, 0x00, 0x00, 0x00, 0x00, 0x00, 0xc0, 0x5a, 0x45, 0x00,
        0x00, 0x80, 0x3f, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x0c, 0x43, 0xcd, 0xcc,
        0x4c, 0x3f, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
    ]

    highest = max(abs(float(bass)), abs(float(mid)), abs(float(treble)))
    total_bytes = encode_eq_float(-highest, True)
    
    # Set total gain at offset 1
    for i in range(4): payload[1 + i] = total_bytes[i]

    values = [float(mid), float(treble), float(bass)]
    
    for idx, val in enumerate(values):
        offset = 6 + (idx * 13)
        val_bytes = encode_eq_float(val, False)
        for i in range(4): payload[offset + i] = val_bytes[i]
        
    return payload
